		<h2 id="title"> My Design Philosophy: Aaron's Design Cycle.</h2>

		<div id="paragraph"> 
			<p id="p1">
					 It is probably bold to call it my design cycle, 
					 because it is a variation of the “FDCR” (framing, diverging, converging, representing) design process taught in University of Toronto, Engineering science, Praxis I. Reasoning behind this variation is based on my own engineering experiences. Check out the <a href="#"> “information delivery system” projects </a> and the <a href="#"> “autonomous food-packing machine” project </a> I have done to see how I implemented this design cycle.  
					<Br/>
					<Br/>
					<Br/> 
			</p>

			<img id="pic1" src="cycle.jpg" width=700 height=400> </img>

			<h5> The direction matters: </h5>
			<p id="p2">
					 Starting with a new engineering project, I start at the “requirement framing” stage. If then I move to the “idea generation” stage, and then the “decision making” stage, following a clockwise direction, I am <span>advancing the cycle </span>. On the other hand, if I follow a counter-clock wise direction, for example, going back to "requirement framing" stage, (in other words, reframing requirement) after idea generation or decision making, I am <span> pushing back the cycle </span>. 
					 <Br/>
					 <Br/>
					 <Br/>
			</p>

			<h5> The standing-alone element, prototyping: </h5>
			<p id="p3">
					 Prototyping is centered in the whole design process because it is crucial in all design period. Essentially, <span>prototyping is a way to convey information either to teammates or to clients. The format of a prototype can be a physical model, a graph, a sketch, some text description, and even a mathematical formula.</span> Depending on the design, prototypes may have very different form. But they are valid as long as they help clarify the design or idea. For example, in idea generation period, if I have sketches showing my idea, they are prototypes. Another example, once our design was a beam bridge, our clients needed to see how the bridge we designed could optimize loading capacity. So, a mathematical derivation was shown as a prototype which demonstrated the validity of the design.
					 <Br/>
					 <Br/>
			</p>

			<p id="p4">
					 <span>In some cases, a physical model as prototype is required.</span> For example, once our clients required us to show how the autonomous food packing machine we designed operated. So, a physical model was needed to show the operation. Another example, for our “info delivery system design project”, at the stage of idea evaluation, we had to test water-proof ability on different types of paper. That can only be done if we have different kinds of paper (physical prototypes) in front of us.   
					 <Br/>
					 <Br/>
			</p>

			<p id="p5">
					 <span>Prototypes have different fidelity.</span> A low-fidelity prototype is an easy-to-build prototype that (usually in a simplified way) simulate the design, the idea, a part of the design, or a part of the idea. It might be used for evaluation or representing primitive ideas. On the contrast, a high-fidelity prototype is a more accurate and detailed representation of the design. It is usually used toward the end of the design process, for example during client validation period. As a rule of thumb, always have multiple low-fidelity prototypes before spending much time and recourses building high-fidelity prototypes.  
					 <Br/>
					 <Br/>
					 <Br/>
			</p>

			<h5> Requirement Framing: </h5>
			<p id="p6">
					 Requirement framing usually involves <span>stakeholder identification and prioritization</span>. Stakeholders are slightly different than clients, because clients usually only refer to people. While <span>stakeholders include anyone and anything that might be both positively and negatively affected by the design</span>. For example, once I designed a booklet for my “info delivery system” project, then environment could be identified as stakeholder. Because if I chose to print a lot of paper using non-recyclable paper, it would be bad for the environment.
					 <Br/>
					 <Br/>
			</p>

			<p id="p7">
					 <span>One design project can have many stakeholders, but it is not necessary to consider them all.</span> Engineers can choose to prioritize some stakeholders which have <span>high influence</span> or <span>high interest</span> in the design. Depending on the “scope” of the design, engineers can also “out-scope” some minor stakeholders.     
					 <Br/>
					 <Br/>
			</p>

			<p id="p8">
					 Once stakeholders are identified and probably prioritized, requirements need to be identified and potentially prioritized as well. <span>Requirements are identified based on the needs of the stakeholders</span>. For example, stakeholders needed to carry around the booklet we designed, so portability was one of the key requirements. However, requirements like “the booklets need to be portable” was not acceptable, because the definition of portability was ambiguous. In contrast, an acceptable requirement would be “the weight of the booklets, measured in pounds, is minimized”. In some cases, the requirement can be drawn from laws, engineering standard or engineering guidelines. So, <span>requirements identification requires massive research in addition to stakeholder consultation.</span>   
					 <Br/>
					 <Br/>
			</p>

			<p id="p9">
					 The identified requirements usually need to be prioritized. Here introduced a very powerful engineering tool for prioritization (of stakeholders or requirements), called <span>pairwise comparison.</span> For example,   
					 <Br/>
					 <Br/>
			</p>

			<img id="pic2" src="pairwise.png" height="400" width="1000"></img>

			<p id="p10">
					 five requirements, safe, aesthetics, cost effective, practical and sustainability, needs to be prioritized. Simply put a “1” in “Safe” row and “Aesthetics” column, if safety is more important than aesthetics. Otherwise put a “0” to indicate safety is not as important as aesthetics. Then, after filling in the table, one can easily see which among the 5 objectives should be prioritized based on the result in the “Rank” column. (Notably, bigger rank means more importance or higher prioritization.) This tool is my all-time favourite because of its simplicity. :)
					 <Br/>
					 <Br/>
			</p>

			<p id="p11">
					 <span>Occasionally, the design might be pushed back to requirement framing stage from a more advanced stage in the cycle</span>. For example, one might find the requirements are impossible to achieve after idea generation. Or, clients might not approve the design at client validation stage. Or, engineers might get a RFP (request for proposal) from stakeholder, but the framing given in the RFP might not be convincing or credible. For any of these cases, requirements reframing might include prioritizing different stakeholders and requirements; clarifying or eliminating currently considered stakeholders and requirements; or adding more requirements and stakeholders.   
					 <Br/>
					 <Br/>
					 <Br/>
			</p>


			<h5> Idea Generation: </h5>
			<p id="p12">
					 With requirements in mind, engineers are supposed to come up with as many ideas as possible. Notably, <span>at this stage, one should focus on quantity instead of quality of the ideas.</span> Because over-thinking the quality of the idea might impede the flow of thoughts. Many idea generation tools are available, my common-used ones are <a href="https://www.mindtools.com/pages/article/newCT_02.htm" target="_blank"> SCAMPER</a>, <span>brainstorming</span> and <span>morphology chart</span> or <span>morph chart</span>. 
					 <Br/>
					 <Br/>
			</p>

			<p id="p13">
					 Among the three, morph chart is my favored one, because it is extremely powerful. Following is an example morph chart which I used for my “info delivery system” design project.        
					 <Br/>
					 <Br/>
			</p>

			<img id="pic3" src="morph.png" height="600" width="1000"></img>

			<p id="p14">
					 As the example shows, we had different ideas for each component of the entire design. We thought about using cell phone app for info platform, using a FAQ system to communicate with staff etc. Then we could combine different ideas from each component to create many design alternatives.     :)
					 <Br/>
					 <Br/>
					 <Br/>
			</p>

			<h5> Idea Evaluation: </h5>
			<p id="p15">
					 <span> At this stage, a detailed testing protocol for each (at least important) requirement is required </span>. For example, recall the weight of the booklet example. At this stage, there must be a well-defined algorithm to determine the weight of all booklet design ideas. Calculation and physical prototyping are often involved. In case the evaluation proves low-quality of the generated ideas, we may choose to push back the design cycle to idea generation stage or even to requirement framing stage. 
					 <Br/>
					 <Br/>
					 <Br/>
			</p>
			<h5> Decision Making: </h5>
			<p id="p16">
					 Now, all ideas have been evaluated. A <span>rating matrix</span> might be a good way to organize all the evaluation result. Below is an example rating matrix I used for paper material selection. As seen in the example, all the requirements are listed in the first row (thickness of the paper, the paper needs to have coating … etc.). And all alternatives or candidates are listed in the first column. To select a candidate, firstly eliminate ideas that met fatal constraints. For example, in our project, stakeholders required that the paper material must have coating. Then cougar and Rolland Enviro Satin were off the table. Next, compare each candidate using the most prioritized requirements first, and then the second prioritized requirements, at last, the least prioritized requirements. 
					 <Br/>
					 <Br/>
			</p>

			<img id="pic4" src="matrix.png" height="700" width="1000"></img>

			<p id="p17">
					 Some other decision making too involves numbers and calculations, such as <a href="#" target="_blank"> AHP (Analytical Hierarchy Process) </a> and <a href="#" target="_blank"> utility function</a>. These decision-making tools provide an algorithm so that you can calculate what your decision should be. I no longer use these kind of tools, because these tools unnecessarily complicate the decision-making process. Plus, the calculation algorithm is not very convincing to me. 
					 <Br/>
					 <Br/>
					 <Br/>
			</p>
			
			<h5> Client (Stakeholders) Validation: </h5>
			<p id="p18">
					 For the first time going through the design cycle, all ideas and decisions are probably general without too much detail. At this stage, system integration and debugging might not be needed. So, we shall save this the discussion of integration and debugging for later detailed design cycle where integration and debugging is necessary. 
					 <Br/>
					 <Br/>
			</p>

			<p id="p19">
					 As the name implied, this is the stage where we will present our design to stakeholders. <span>In addition to stakeholder approval, some testing on high-fidelity prototypes are necessary to prove the validity of the design.</span> For example, recall the booklet we designed, we arranged potential users to use our booklet prototype. We did timing on how long do they need to locate specific information in the booklet. The test reassured that the design achieved the usability requirement: “minimize time needed to locate information within the system”.            
					 <Br/>
					 <Br/>
					 <Br/>
			</p>

			<h5> Next Cycle: </h5>
			<p id="p20">
					 Usually, <span>a complete engineering project require going through multiple design cycles. The later cycles would be focusing on much more details than the first cycle</span>. The "autonomous food-packing machine" design project I did is an example. During first cycle, high-level design was settled including the dimension, the structure, the operation process of the machine. After first cycle, we went through a second cycle, generated ideas about which types of censor we could use, made a decision on the selection of censors, and validated the censor design to the team’s software developer (it’s me actually) who would program the censor. Then, we had another cycle determining motors that drive the gears, another cycle determining the algorithm that handling user input and so on… As the example illustrated, during the later cycles, <span>stakeholder might change to teammate or other sub-teams doing the same project. And the ideas and decisions are about a detailed part of the entire design. </span> 
					 <Br/>
					 <Br/>
					 <Br/>
			</p>
			<h5> System Integration and Debugging: </h5>
			<p id="p21">
					 <span>System integration and debugging stage happens in detailed design cycles after the team integrate the detailed decision into the high-level design or system.</span> Debugging are needed because there are usually bugs. For example, after we added the gear-driving circuits and the motor driving circuits into the autonomous food-packing machine. The signal generated from the 2 circuits interfered with each other. Therefore, some debugging solution is required. See the <a href="#"> “autonomous food-packing machine” page </a> to find out the exact debugging solution. :)   
					 <Br/>
					 <Br/>
					 <Br/>
					 <Br/>
					 <Br/>
			</p>



